def __getattr__(name: str) -> Any:
    if name in _COMPAT_EXPORTS:
        warnings.warn(
            f"gaia.engine.lang.{name} is deprecated. Prefer current v0.5 "
            "verbs from gaia.engine.lang; if you must keep this legacy API "
            f"during migration, import gaia.engine.lang.compat.{name} explicitly.",
            DeprecationWarning,
            stacklevel=2,
        )
        compat = import_module("gaia.engine.lang.compat")
        value = getattr(compat, name)
        globals()[name] = value
        return value
    raise AttributeError(f"module 'gaia.engine.lang' has no attribute {name!r}")


